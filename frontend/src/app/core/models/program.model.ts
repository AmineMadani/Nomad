export interface Program {
  id: number; // 1 - 2 ...
  label: string; // Random
  startDate: Date; // Random
  endDate: Date; // Random
  assetType: string[]; // aep_canalisation / aep_vanne
  action: string; // Random action for something like water pipes etc...
  status: string; // CREE / TERMINE / EN_COURS
  viewingPreference: string; // color
  contract: string; // Random string like 'S813E', 'A256B'....
  programRenewal: boolean; // Random between true or false
  periodRenewal: string; // Random string like '1A', '2An', '3A', '3An'.....'XAn'
  creationDate: Date; // Random date
  updateDate: Date; // Random Date
  lastEditor: string; // Random name
  creator: string; // Random name
  comment: string; // Random comment
}

